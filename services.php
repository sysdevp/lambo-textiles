Note: In the Tally.NET framework it is essential to connect the Client and the Server to Tally.NET in order to Synchronise data.
<strong>Synchronisation using Private Network</strong><br>
From Tally.ERP 9 onwards, Direct Client and Direct Server capability for Synchronisation through private network (also referred as IP Sync) is a default feature available in the product. The process for Data Synchronisation remains the same while doing the Sync using Direct â€“ Client and Server capability (or IP Sync). <br>
